public class Loops {
    public static void main(String[] args) {
        int x = 0;
            while(x < 1) {
                System.out.println("While loop");
                x++;
            }

        int y = 100;
            do{
                System.out.println("Do While");
                y--;
            }while(y>100);

            for(int i=1;i<=2;i++){
                System.out.println("For");
            }
    }
}
